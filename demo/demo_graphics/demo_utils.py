def demo_rotate_geometry():
    v = [0, 1]
    v2 = rotate_geometry(v, np.pi / 2)
    plot_arrow([0, v[0]], [0, v[1]], "b-")
    plot_arrow([0, v2[0]], [0,v2[1]], "r-")
    plt.axis("equal")
    plt.show()

